require 'json'
require 'open-uri'

puts "Destroying existing ingredients"
Ingredient.destroy_all
puts "Accessing the API"
url = 'https://www.thecocktaildb.com/api/json/v1/1/list.php?i=list'
ingredients_serialized = open(url).read
ingredients = JSON.parse(ingredients_serialized)['drinks']
puts "Adding the ingredients"
ingredients.each { |ingredient| Ingredient.create(name: ingredient['strIngredient1']) }
puts "Added #{Ingredient.count} ingredients to the DB"
